export const calculateRikkesSummary = (records: RikkesRecord[]): RikkesSummary => {
  const now = new Date();
  const thirtyDaysFromNow = new Date(now.getTime() + 30 * 24 * 60 * 60 * 1000);

  return {
    totalPemeriksaan: records.length,
    kategoriA: records.filter(r => r.kategoriKesehatan === 'A').length,
    kategoriB: records.filter(r => r.kategoriKesehatan === 'B').length,
    kategoriC: records.filter(r => r.kategoriKesehatan === 'C').length,
    kategoriD: records.filter(r => r.kategoriKesehatan === 'D').length,
    kategoriE: records.filter(r => r.kategoriKesehatan === 'E').length,
    fitToFly: records.filter(r => r.status === 'Fit').length,
    fitWithRestriction: records.filter(r => r.status === 'Fit with Restriction').length,
    unfit: records.filter(r => r.status === 'Temporarily Unfit' || r.status === 'Permanently Unfit').length,
    expiringSoon: records.filter(r => {
      const expiryDate = new Date(r.masaBerlaku);
      return expiryDate <= thirtyDaysFromNow && expiryDate >= now;
    }).length
  };
};

